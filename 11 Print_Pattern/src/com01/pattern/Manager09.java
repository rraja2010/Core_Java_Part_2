package com01.pattern;

public class Manager09
{
	public static void main(String[] args)
	{
		for (char ch = 'E'; ch >='A'; ch--)
		{
			for (char c = 'E'; c >='A'; c--)
			{
				System.out.print(c+" ");
			}	
			System.out.println();
		}
		System.out.println("***THANK YOU!!***");
	}
}
