public class Manager05
{
	public static void main(String[] args)
	{
		for (char ch = 'A'; ch <='E'; ch++)
		{
			for (char c = 'A'; c <='E'; c++)
			{
				System.out.print(c+" ");
			}	
			System.out.println();
		}
		System.out.println("***THANK YOU!!***");
	}
}
