package com.switchstmt;
//Normal input and normal output
public class A {
	public static void main(String[] args) {
		int a = 10;
		switch (a) {
		case 10:
			System.out.println("Case 10");
			break;
		case 20:
			System.out.println("Case 20");
			break;
		case 30:
			System.out.println("Case 30");
			break;

		default:
			System.out.println("Default");
			break;
		}
		
	}
}
