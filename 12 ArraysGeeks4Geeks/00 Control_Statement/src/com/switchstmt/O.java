package com.switchstmt;

public class O {
	public static void main(String[] args) {
		String a = "aaa";
		switch (a) {
		case "aaa":
			System.out.println("Case aaa");
			break;
		case "bbb":
			System.out.println("Case bbb");
			break;
		case "ccc":
			System.out.println("Case ccc");
			break;
		default:
			System.out.println("Default");
			break;
		
		
		}
	}
}
