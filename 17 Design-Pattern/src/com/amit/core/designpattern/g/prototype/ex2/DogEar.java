package com.amit.core.designpattern.g.prototype.ex2;

public class DogEar {
	public int noOfEar;

	public void setNoOfEar(int noOfEar) {
		this.noOfEar = noOfEar;
	}

	public int getNoOfEar() {
		return noOfEar;
	}

	@Override
	public String toString() {
		return "DogEar [noOfEar=" + noOfEar + "]";
	}

}
